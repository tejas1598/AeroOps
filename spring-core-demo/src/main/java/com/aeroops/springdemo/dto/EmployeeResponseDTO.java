package com.aeroops.springdemo.dto;

public class EmployeeResponseDTO
{
    Long id;
    String name;
    int age;

    public EmployeeResponseDTO() {}

    public EmployeeResponseDTO(Long id, String name, int age) {
        this.id = id;
        this.name = name;
        this.age = age;
    }

    public Long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
